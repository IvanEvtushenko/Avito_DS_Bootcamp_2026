def mini_articles() -> pd.DataFrame:
    df = pd.DataFrame(_ARTICLES, columns=["article_id", "title", "body"])
    df["article_id"] = df["article_id"].astype("int64")
    return df


def mini_calibration() -> pd.DataFrame:
    df = pd.DataFrame(_CALIBRATION, columns=["query_id", "query_text", "ground_truth"])
    df["query_id"] = df["query_id"].astype("int64")
    return df


def mini_test() -> pd.DataFrame:
    df = pd.DataFrame(_TEST, columns=["query_id", "query_text"])
    df["query_id"] = df["query_id"].astype("int64")
    return df


def write_mini_dataset(directory: str | Path) -> Path:
    """Записать три feather-файла (articles/calibration/test) в каталог."""
    directory = Path(directory)
    directory.mkdir(parents=True, exist_ok=True)
    mini_articles().to_feather(directory / "articles.f")
    mini_calibration().to_feather(directory / "calibration.f")
    mini_test().to_feather(directory / "test.f")
    return directory
